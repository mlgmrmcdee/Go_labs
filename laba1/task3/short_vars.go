package main

import "fmt"

func main() {
    age := 20
    pi := 3.14
    name := "mlgmrmcdee"
    isGoCool := true

    fmt.Println("Целое число (int):", age)
    fmt.Println("Число с плавающей точкой (float64):", pi)
    fmt.Println("Строка (string):", name)
    fmt.Println("Булево значение (bool):", isGoCool)
}
