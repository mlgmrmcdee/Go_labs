// Программа вычисляет среднее значение трёх чисел
package main

import (
	"fmt"
)

func main() {
	var a, b, c float64

	// Ввод трёх чисел через пробел
	fmt.Print("Введите три числа через пробел: ")
	fmt.Scan(&a, &b, &c)

	// Вычисление среднего значения
	average := (a + b + c) / 3

	// Вывод результата с двумя знаками после запятой
	fmt.Printf("Среднее значение: %.2f\n", average)
}
